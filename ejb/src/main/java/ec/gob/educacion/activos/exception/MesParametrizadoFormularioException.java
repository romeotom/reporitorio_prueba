package ec.gob.educacion.activos.exception;

public class MesParametrizadoFormularioException extends Exception {
	
	private static final long serialVersionUID = 7390423866782335568L;

	public MesParametrizadoFormularioException() {
		super();
	}

	public MesParametrizadoFormularioException(String message, Throwable cause) {
		super(message, cause);
	}

	public MesParametrizadoFormularioException(String message) {
		super(message);
	}

	public MesParametrizadoFormularioException(Throwable cause) {
		super(cause);
	}
}
