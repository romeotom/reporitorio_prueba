package ec.gob.educacion.activos.exception;

public class RecursoDigitalException extends Exception {
	
	private static final long serialVersionUID = 7390423866782335568L;

	public RecursoDigitalException() {
		super();
	}

	public RecursoDigitalException(String message, Throwable cause) {
		super(message, cause);
	}

	public RecursoDigitalException(String message) {
		super(message);
	}

	public RecursoDigitalException(Throwable cause) {
		super(cause);
	}
}
