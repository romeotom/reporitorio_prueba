package ec.gob.educacion.activos.exception;

public class ResponsableException extends Exception {
	
	private static final long serialVersionUID = 7390423866782335568L;

	public ResponsableException() {
		super();
	}

	public ResponsableException(String message, Throwable cause) {
		super(message, cause);
	}

	public ResponsableException(String message) {
		super(message);
	}

	public ResponsableException(Throwable cause) {
		super(cause);
	}
}
