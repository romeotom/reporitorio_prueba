package ec.gob.educacion.activos.exception;

public class PromedioEstudianteException extends Exception {
	
	private static final long serialVersionUID = 7390423866782335568L;

	public PromedioEstudianteException() {
		super();
	}

	public PromedioEstudianteException(String message, Throwable cause) {
		super(message, cause);
	}

	public PromedioEstudianteException(String message) {
		super(message);
	}

	public PromedioEstudianteException(Throwable cause) {
		super(cause);
	}
}
