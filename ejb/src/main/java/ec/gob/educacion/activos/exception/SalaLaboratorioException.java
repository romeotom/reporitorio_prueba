package ec.gob.educacion.activos.exception;

public class SalaLaboratorioException extends Exception {
	
	private static final long serialVersionUID = 7390423866782335568L;

	public SalaLaboratorioException() {
		super();
	}

	public SalaLaboratorioException(String message, Throwable cause) {
		super(message, cause);
	}

	public SalaLaboratorioException(String message) {
		super(message);
	}

	public SalaLaboratorioException(Throwable cause) {
		super(cause);
	}
}
