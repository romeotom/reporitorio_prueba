package ec.gob.educacion.activos.exception;

public class TalentoHumanoException extends Exception {
	
	private static final long serialVersionUID = 7390423866782335568L;

	public TalentoHumanoException() {
		super();
	}

	public TalentoHumanoException(String message, Throwable cause) {
		super(message, cause);
	}

	public TalentoHumanoException(String message) {
		super(message);
	}

	public TalentoHumanoException(Throwable cause) {
		super(cause);
	}
}
