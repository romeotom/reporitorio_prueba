package ec.gob.educacion.activos.exception;

public class NumeroDocenteException extends Exception {
	
	private static final long serialVersionUID = 7390423866782335568L;

	public NumeroDocenteException() {
		super();
	}

	public NumeroDocenteException(String message, Throwable cause) {
		super(message, cause);
	}

	public NumeroDocenteException(String message) {
		super(message);
	}

	public NumeroDocenteException(Throwable cause) {
		super(cause);
	}
}
