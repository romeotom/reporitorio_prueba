package ec.gob.educacion.activos.exception;

public class AccesibilidadServicioException extends Exception {
	
	private static final long serialVersionUID = 7390423866782335568L;

	public AccesibilidadServicioException() {
		super();
	}

	public AccesibilidadServicioException(String message, Throwable cause) {
		super(message, cause);
	}

	public AccesibilidadServicioException(String message) {
		super(message);
	}

	public AccesibilidadServicioException(Throwable cause) {
		super(cause);
	}
}
