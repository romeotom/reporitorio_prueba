package ec.gob.educacion.activos.exception;

public class CuartoServidoreException extends Exception {
	
	private static final long serialVersionUID = 7390423866782335568L;

	public CuartoServidoreException() {
		super();
	}

	public CuartoServidoreException(String message, Throwable cause) {
		super(message, cause);
	}

	public CuartoServidoreException(String message) {
		super(message);
	}

	public CuartoServidoreException(Throwable cause) {
		super(cause);
	}
}
