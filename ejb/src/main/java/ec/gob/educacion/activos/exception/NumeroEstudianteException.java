package ec.gob.educacion.activos.exception;

public class NumeroEstudianteException extends Exception {
	
	private static final long serialVersionUID = 7390423866782335568L;

	public NumeroEstudianteException() {
		super();
	}

	public NumeroEstudianteException(String message, Throwable cause) {
		super(message, cause);
	}

	public NumeroEstudianteException(String message) {
		super(message);
	}

	public NumeroEstudianteException(Throwable cause) {
		super(cause);
	}
}
