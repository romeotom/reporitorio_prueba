package ec.gob.educacion.activos.exception;

public class JornadaEducacionException extends Exception {
	
	private static final long serialVersionUID = 7390423866782335568L;

	public JornadaEducacionException() {
		super();
	}

	public JornadaEducacionException(String message, Throwable cause) {
		super(message, cause);
	}

	public JornadaEducacionException(String message) {
		super(message);
	}

	public JornadaEducacionException(Throwable cause) {
		super(cause);
	}
}
