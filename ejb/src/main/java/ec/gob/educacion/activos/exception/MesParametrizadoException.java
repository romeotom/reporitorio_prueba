package ec.gob.educacion.activos.exception;

public class MesParametrizadoException extends Exception {
	
	private static final long serialVersionUID = 7390423866782335568L;

	public MesParametrizadoException() {
		super();
	}

	public MesParametrizadoException(String message, Throwable cause) {
		super(message, cause);
	}

	public MesParametrizadoException(String message) {
		super(message);
	}

	public MesParametrizadoException(Throwable cause) {
		super(cause);
	}
}
